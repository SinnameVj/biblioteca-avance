-- ============================================================
-- 1. TABLA: categories
-- ============================================================
CREATE TABLE IF NOT EXISTS categories (
    id   BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT UNIQUE NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc'::text, NOW()) NOT NULL
);

-- ============================================================
-- 2. TABLA: profiles  (extiende auth.users de Supabase)
-- ============================================================
CREATE TABLE IF NOT EXISTS profiles (
    id       UUID REFERENCES auth.users ON DELETE CASCADE PRIMARY KEY,
    name     TEXT NOT NULL,
    username TEXT UNIQUE,
    role     TEXT NOT NULL DEFAULT 'estudiante'
               CHECK (role IN ('admin', 'profesor', 'estudiante')),
    phone    TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc'::text, NOW()) NOT NULL
);

-- ============================================================
-- 3. TABLA: books
-- ============================================================
CREATE TABLE IF NOT EXISTS books (
    id               BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    title            TEXT NOT NULL,
    author           TEXT NOT NULL,
    category         TEXT REFERENCES categories(name) ON UPDATE CASCADE ON DELETE SET NULL,
    isbn             TEXT UNIQUE,
    cover            TEXT,
    total_copies     INTEGER DEFAULT 1,
    available_copies INTEGER DEFAULT 1,
    penalty_rate     NUMERIC(10,2) DEFAULT 1.00,   -- S/ por día de mora
    created_at       TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc'::text, NOW()) NOT NULL
);

-- ============================================================
-- 4. TABLA: loans
-- ============================================================
CREATE TABLE IF NOT EXISTS loans (
    id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    book_id             BIGINT REFERENCES books(id) ON DELETE CASCADE,
    user_id             UUID   REFERENCES auth.users ON DELETE CASCADE,
    phone               TEXT,
    request_date        TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc'::text, NOW()) NOT NULL,
    borrow_date         TIMESTAMP WITH TIME ZONE,
    due_date            TIMESTAMP WITH TIME ZONE,
    return_date         TIMESTAMP WITH TIME ZONE,           -- fecha real de devolución
    status              TEXT NOT NULL DEFAULT 'pending_pickup'
                          CHECK (status IN (
                            'pending_pickup',
                            'active',
                            'overdue',
                            'pending_return',
                            'returned'
                          )),
    is_extending        BOOLEAN DEFAULT FALSE,
    extension_requested BOOLEAN DEFAULT FALSE,
    manual_penalty      NUMERIC(10,2) DEFAULT 0,   -- ajuste manual de mora (admin)
    paid_penalty        NUMERIC(10,2) DEFAULT 0,   -- mora cobrada al momento de devolución
    created_at          TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc'::text, NOW()) NOT NULL
);

-- ============================================================
-- 5. TABLA: reservations
-- ============================================================
CREATE TABLE IF NOT EXISTS reservations (
    id         BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    book_id    BIGINT REFERENCES books(id) ON DELETE CASCADE,
    user_id    UUID   REFERENCES auth.users ON DELETE CASCADE,
    priority   BOOLEAN DEFAULT FALSE,
    date       TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc'::text, NOW()) NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc'::text, NOW()) NOT NULL
);

-- ============================================================
-- 6. TABLA: held_books  (libro apartado esperando al usuario)
-- ============================================================
CREATE TABLE IF NOT EXISTS held_books (
    id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    book_id       BIGINT REFERENCES books(id) ON DELETE CASCADE,
    user_id       UUID   REFERENCES auth.users ON DELETE CASCADE,
    assigned_date TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc'::text, NOW()) NOT NULL,
    created_at    TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc'::text, NOW()) NOT NULL
);

-- ============================================================
-- 7. FUNCTION + TRIGGER: crear perfil automáticamente al registrarse
-- ============================================================
CREATE OR REPLACE FUNCTION public.handle_new_user()
RETURNS TRIGGER AS $$
BEGIN
  INSERT INTO public.profiles (id, name, username, role)
  VALUES (
    new.id,
    COALESCE(new.raw_user_meta_data->>'name',     'Nuevo Usuario'),
    COALESCE(new.raw_user_meta_data->>'username',  new.email),
    COALESCE(new.raw_user_meta_data->>'role',      'estudiante')
  );
  RETURN NEW;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

DROP TRIGGER IF EXISTS on_auth_user_created ON auth.users;
CREATE TRIGGER on_auth_user_created
  AFTER INSERT ON auth.users
  FOR EACH ROW EXECUTE PROCEDURE public.handle_new_user();

