-- 8. ROW LEVEL SECURITY (RLS)
-- ============================================================
ALTER TABLE categories  ENABLE ROW LEVEL SECURITY;
ALTER TABLE profiles    ENABLE ROW LEVEL SECURITY;
ALTER TABLE books       ENABLE ROW LEVEL SECURITY;
ALTER TABLE loans       ENABLE ROW LEVEL SECURITY;
ALTER TABLE reservations ENABLE ROW LEVEL SECURITY;
ALTER TABLE held_books  ENABLE ROW LEVEL SECURITY;

-- -------- POLICIES: categories --------
DROP POLICY IF EXISTS "categories_select_all"  ON categories;
DROP POLICY IF EXISTS "categories_admin_write"  ON categories;

-- Cualquier usuario autenticado puede leer categorías
CREATE POLICY "categories_select_all"
  ON categories FOR SELECT
  USING (auth.role() = 'authenticated');

-- Solo admins pueden crear/editar/borrar categorías
CREATE POLICY "categories_admin_write"
  ON categories FOR ALL
  USING (
    EXISTS (SELECT 1 FROM profiles WHERE id = auth.uid() AND role = 'admin')
  )
  WITH CHECK (
    EXISTS (SELECT 1 FROM profiles WHERE id = auth.uid() AND role = 'admin')
  );

-- -------- POLICIES: profiles --------
DROP POLICY IF EXISTS "profiles_select_own"     ON profiles;
DROP POLICY IF EXISTS "profiles_admin_all"      ON profiles;
DROP POLICY IF EXISTS "profiles_update_own"     ON profiles;

-- Cada usuario puede leer su propio perfil
CREATE POLICY "profiles_select_own"
  ON profiles FOR SELECT
  USING (id = auth.uid());

-- Cada usuario puede actualizar su propio perfil (nombre y teléfono)
CREATE POLICY "profiles_update_own"
  ON profiles FOR UPDATE
  USING (id = auth.uid())
  WITH CHECK (id = auth.uid());

-- El admin puede leer y modificar todos los perfiles
CREATE POLICY "profiles_admin_all"
  ON profiles FOR ALL
  USING (
    EXISTS (SELECT 1 FROM profiles WHERE id = auth.uid() AND role = 'admin')
  )
  WITH CHECK (
    EXISTS (SELECT 1 FROM profiles WHERE id = auth.uid() AND role = 'admin')
  );

-- -------- POLICIES: books --------
DROP POLICY IF EXISTS "books_select_all"    ON books;
DROP POLICY IF EXISTS "books_admin_write"   ON books;
DROP POLICY IF EXISTS "books_user_update"   ON books;

-- Todos los autenticados pueden ver los libros
CREATE POLICY "books_select_all"
  ON books FOR SELECT
  USING (auth.role() = 'authenticated');

-- Admin puede crear, actualizar y eliminar libros
CREATE POLICY "books_admin_write"
  ON books FOR ALL
  USING (
    EXISTS (SELECT 1 FROM profiles WHERE id = auth.uid() AND role = 'admin')
  )
  WITH CHECK (
    EXISTS (SELECT 1 FROM profiles WHERE id = auth.uid() AND role = 'admin')
  );

-- Usuarios autenticados pueden actualizar available_copies (necesario para préstamos)
CREATE POLICY "books_user_update_copies"
  ON books FOR UPDATE
  USING (auth.role() = 'authenticated')
  WITH CHECK (auth.role() = 'authenticated');

-- -------- POLICIES: loans --------
DROP POLICY IF EXISTS "loans_select_own"    ON loans;
DROP POLICY IF EXISTS "loans_insert_own"    ON loans;
DROP POLICY IF EXISTS "loans_update_own"    ON loans;
DROP POLICY IF EXISTS "loans_admin_all"     ON loans;

-- Cada usuario puede ver sus propios préstamos
CREATE POLICY "loans_select_own"
  ON loans FOR SELECT
  USING (user_id = auth.uid());

-- Cada usuario puede crear un préstamo para sí mismo
CREATE POLICY "loans_insert_own"
  ON loans FOR INSERT
  WITH CHECK (user_id = auth.uid());

-- Cada usuario puede actualizar sus propios préstamos (ej: solicitar devolución)
CREATE POLICY "loans_update_own"
  ON loans FOR UPDATE
  USING (user_id = auth.uid())
  WITH CHECK (user_id = auth.uid());

-- Admin puede ver y gestionar todos los préstamos
CREATE POLICY "loans_admin_all"
  ON loans FOR ALL
  USING (
    EXISTS (SELECT 1 FROM profiles WHERE id = auth.uid() AND role = 'admin')
  )
  WITH CHECK (
    EXISTS (SELECT 1 FROM profiles WHERE id = auth.uid() AND role = 'admin')
  );

-- -------- POLICIES: reservations --------
DROP POLICY IF EXISTS "reservations_select_own"  ON reservations;
DROP POLICY IF EXISTS "reservations_insert_own"  ON reservations;
DROP POLICY IF EXISTS "reservations_delete_own"  ON reservations;
DROP POLICY IF EXISTS "reservations_admin_all"   ON reservations;

-- Cada usuario puede ver sus propias reservas
CREATE POLICY "reservations_select_own"
  ON reservations FOR SELECT
  USING (user_id = auth.uid());

-- Cada usuario puede reservar para sí mismo
CREATE POLICY "reservations_insert_own"
  ON reservations FOR INSERT
  WITH CHECK (user_id = auth.uid());

-- Cada usuario puede cancelar su propia reserva
CREATE POLICY "reservations_delete_own"
  ON reservations FOR DELETE
  USING (user_id = auth.uid());

-- Admin gestiona todas las reservas
CREATE POLICY "reservations_admin_all"
  ON reservations FOR ALL
  USING (
    EXISTS (SELECT 1 FROM profiles WHERE id = auth.uid() AND role = 'admin')
  )
  WITH CHECK (
    EXISTS (SELECT 1 FROM profiles WHERE id = auth.uid() AND role = 'admin')
  );

-- -------- POLICIES: held_books --------
DROP POLICY IF EXISTS "held_select_own"   ON held_books;
DROP POLICY IF EXISTS "held_admin_all"    ON held_books;

-- Cada usuario puede ver libros apartados para él
CREATE POLICY "held_select_own"
  ON held_books FOR SELECT
  USING (user_id = auth.uid());

-- Admin gestiona todos los held_books
CREATE POLICY "held_admin_all"
  ON held_books FOR ALL
  USING (
    EXISTS (SELECT 1 FROM profiles WHERE id = auth.uid() AND role = 'admin')
  )
  WITH CHECK (
    EXISTS (SELECT 1 FROM profiles WHERE id = auth.uid() AND role = 'admin')
  );

-- Usuarios autenticados pueden insertar/eliminar held_books
-- (necesario porque el código del cliente gestiona la cola)
CREATE POLICY "held_auth_insert"
  ON held_books FOR INSERT
  WITH CHECK (auth.role() = 'authenticated');

CREATE POLICY "held_auth_delete"
  ON held_books FOR DELETE
  USING (auth.role() = 'authenticated');

-- ============================================================
-- 9. STORAGE: bucket 'book-covers' (portadas de libros)
-- ============================================================
INSERT INTO storage.buckets (id, name, public)
VALUES ('book-covers', 'book-covers', true)
ON CONFLICT (id) DO NOTHING;

-- Acceso público de lectura
DROP POLICY IF EXISTS "book_covers_public_select" ON storage.objects;
CREATE POLICY "book_covers_public_select"
  ON storage.objects FOR SELECT
  USING (bucket_id = 'book-covers');

-- Solo autenticados pueden subir portadas
DROP POLICY IF EXISTS "book_covers_auth_insert" ON storage.objects;
CREATE POLICY "book_covers_auth_insert"
  ON storage.objects FOR INSERT
  WITH CHECK (bucket_id = 'book-covers' AND auth.role() = 'authenticated');

-- Solo autenticados pueden actualizar portadas
DROP POLICY IF EXISTS "book_covers_auth_update" ON storage.objects;
CREATE POLICY "book_covers_auth_update"
  ON storage.objects FOR UPDATE
  USING (bucket_id = 'book-covers' AND auth.role() = 'authenticated');

-- Solo admins pueden eliminar portadas
DROP POLICY IF EXISTS "book_covers_admin_delete" ON storage.objects;
CREATE POLICY "book_covers_admin_delete"
  ON storage.objects FOR DELETE
  USING (
    bucket_id = 'book-covers'
    AND EXISTS (SELECT 1 FROM profiles WHERE id = auth.uid() AND role = 'admin')
  );

